// User types and interfaces

export type CompanyType = "LTD Company" | "Sole Trader" | "Virtual Address";

export interface PersonalInformation {
  email: string;
  password: string;
  firstName: string;
  middleName?: string;
  surname: string;
  dateOfBirth: string; // dd/mm/yyyy
  phone: string;
  nationality: string;
}

export interface AddressDetails {
  addressLine1: string;
  addressLine2?: string;
  cityTown: string;
  postcodeZip: string;
  country: string;
}

export interface CompanyInformation {
  companyType: CompanyType;
}

export interface UserProfile
  extends PersonalInformation,
    AddressDetails,
    CompanyInformation {
  id?: string;
  role: "admin" | "user";
  createdAt?: string;
  updatedAt?: string;
  isActive?: boolean;
}

export interface UserFormData
  extends PersonalInformation,
    AddressDetails,
    CompanyInformation {
  confirmPassword: string;
  role: "admin" | "user";
  terms: boolean;
}

// Admin types
export interface AdminUser {
  id: string;
  email: string;
  firstName: string;
  surname: string;
  role: "admin" | "user";
  companyType: CompanyType;
  isActive: boolean;
  createdAt: string;
  updatedAt: string;
  lastLogin?: string;
}

export interface ActivityLog {
  id: string;
  userId: string;
  action: string;
  details: string;
  timestamp: string;
  ipAddress?: string;
}

export interface AdminPermissions {
  canCreateUsers: boolean;
  canEditUsers: boolean;
  canDeleteUsers: boolean;
  canViewUsers: boolean;
  canAssignRoles: boolean;
  canExportData: boolean;
  canViewLogs: boolean;
}

